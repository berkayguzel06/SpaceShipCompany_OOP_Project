    public static String[][] fileop(String name){
		 String filePath = name;
	        String[][] userMatrix = null;

	        try (BufferedReader br = new BufferedReader(new FileReader(filePath))) {
	            String line;
	            int numRows = countLines(filePath);
	            userMatrix = new String[numRows][7];

	            int row = 0;
	            while ((line = br.readLine()) != null) {
	                String[] userData = line.split(",");
	                if (userData.length == 7) {
	                    userMatrix[row] = userData;
	                    row++;
	                }
	            }

	        } catch (IOException e) {
	            e.printStackTrace();
	        }
			return userMatrix;
	    }
    private static int countLines(String filePath) throws IOException {
        try (BufferedReader br = new BufferedReader(new FileReader(filePath))) {
            int count = 0;
            while (br.readLine() != null) {
                count++;
            }
            return count;
        }
    }
    
    
