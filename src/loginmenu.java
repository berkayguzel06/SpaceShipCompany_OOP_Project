    private void signinButton1ActionPerformed(java.awt.event.ActionEvent evt) throws FileNotFoundException {//GEN-FIRST:event_signinButton1ActionPerformed
        // TODO add your handling code here:
        String name1 = username1.getText();
        String pass1 = password1.getText().toString();
        if (name1.isEmpty() || pass1.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Name or password cannot be empty", "ERROR", JOptionPane.ERROR_MESSAGE);
        }
        else{
            String[][] users = fileop("users.txt"); // control the username and password from user.txt and start manager page
            Boolean infomatch = false;
            for (int i = 0; i < users.length; i++) {
                if (users[i][0].equals(name1) && users[i][1].equals(pass1) && users[i][2].equals("M")) {
                    infomatch = true;
                    //start manager page
                    dispose();
                    managerPage managerr = new managerPage();
                    managerr.setVisible(true);
                }
            }
            if (infomatch == false) {
                JOptionPane.showMessageDialog(this, "Username or password cannot found or does not match", "ERROR", JOptionPane.ERROR_MESSAGE);
            }
            
        }
    }//GEN-LAST:event_signinButton1ActionPerformed
